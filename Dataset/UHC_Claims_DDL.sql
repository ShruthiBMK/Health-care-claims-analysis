CREATE DATABASE IF NOT EXISTS UHC_Claims;
USE UHC_Claims;


CREATE TABLE patients (
  patient_id INT PRIMARY KEY,
  first_name VARCHAR(50),
  last_name VARCHAR(50),
  gender CHAR(1),
  date_of_birth DATE,
  state VARCHAR(30)
);

CREATE TABLE providers (
  provider_id INT PRIMARY KEY,
  provider_name VARCHAR(100),
  city VARCHAR(50),
  state VARCHAR(30),
  provider_type VARCHAR(50)
);

CREATE TABLE diagnosis (
  diagnosis_code VARCHAR(10) PRIMARY KEY,
  diagnosis_desc VARCHAR(200)
);

CREATE TABLE insurance_plans (
  plan_id INT PRIMARY KEY,
  plan_name VARCHAR(50),
  payer VARCHAR(50),
  coverage_percent DECIMAL(5,2)
);

CREATE TABLE claims (
  claim_id INT PRIMARY KEY,
  patient_id INT,
  provider_id INT,
  plan_id INT,
  diagnosis_code VARCHAR(10),
  claim_date DATE,
  total_cost DECIMAL(10,2),
  amount_covered DECIMAL(10,2),
  claim_status VARCHAR(20),
  FOREIGN KEY (patient_id) REFERENCES patients(patient_id),
  FOREIGN KEY (provider_id) REFERENCES providers(provider_id),
  FOREIGN KEY (plan_id) REFERENCES insurance_plans(plan_id),
  FOREIGN KEY (diagnosis_code) REFERENCES diagnosis(diagnosis_code)
);
